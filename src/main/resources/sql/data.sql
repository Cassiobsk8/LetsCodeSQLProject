CREATE DATABASE IF NOT EXISTS CRM;
CREATE TABLE IF NOT EXISTS CRM.infoEmpresa (
    id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
    empresa VARCHAR(100) NOT NULL,
    cnpj VARCHAR(14) NOT NULL,
    cidade VARCHAR(100) NOT NULL,
    uf VARCHAR(2) NOT NULL
    ) ENGINE=INNODB DEFAULT CHARSET=utf8;
CREATE TABLE IF NOT EXISTS CRM.infoProduto (
    id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
    produto VARCHAR(100) NOT NULL,
    vertical VARCHAR(100) NOT NULL,
    valorBase DECIMAL(10,2) NOT NULL,
    DescricaoItem VARCHAR(200) NOT NULL
    ) ENGINE=INNODB DEFAULT CHARSET=utf8;
CREATE TABLE IF NOT EXISTS CRM.vendas (
    id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
    idProduto INT NOT NULL,
    idEmpresa INT NOT NULL,
    dataCompra DATE NOT NULL,
    valorNegociado DECIMAL(10,2) NOT NULL,
    tipoCompra VARCHAR(100) NOT NULL,
    CONSTRAINT fk_produto FOREIGN KEY (idProduto)
        REFERENCES CRM.infoProduto(id),
    CONSTRAINT fk_empresa FOREIGN KEY (idEmpresa)
        REFERENCES CRM.infoEmpresa(id)
    ) ENGINE=INNODB DEFAULT CHARSET=utf8;
